# Five reserved word definitions:
# if: Used to run code only when a condition is true.
# else: Used to run a different block of code when the if condition is false.
# for: Used to loop through a sequence like a list or range.
# while: Used to repeat code while a condition remains true.
# def: Used to define a function in Python.